const dicts: Record<Lang, Record<string, string>> = { zh, en };

export function getLangDict(lang: Lang): Record<string, string> {
  return dicts[lang];
}

export function t(key: string, lang: Lang, params?: Record<string, string | number>): string {
  let text = dicts[lang][key] || dicts.zh[key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}

// 在服务端组件中获取语言
export async function getLang(cookieHeader: string | null): Promise<Lang> {
  if (cookieHeader) {
    const match = cookieHeader.match(/gwm_lang=(zh|en)/);
    if (match) return match[1] as Lang;
  }
  // 默认中文
  return "zh";
}
